package fr.itris.glips.rtda;

/**
 * the listener to the changes of the animations state (Started, paused, resumed, stopped)
 * 
 * @author ITRIS, Jordi SUC
 */
public abstract class AnimationsStateListener implements Comparable<AnimationsStateListener>{

    /**
     * called the first time an animation is added to the animations handler before the animations handler is started
     */
    public void animationsExistBeforeStarting(){
    	
    }
    
    /**
     * called when the no more animations are available
     */
    public void noMoreAnimations(){
    	
    }
    
    /**
     * called when the animations have been started
     */
    public void animationsStarted(){
    	
    }
    
    /**
     * called when the animations have been paused
     */
    public void animationsPaused(){
    	
    }
    
    /**
     * called when the animations have been resumed
     */
    public void animationsResumed(){
    	
    }
    
    /**
     * called when the animations have been stopped
     */
    public void animationsStopped(){
    	
    }
    
    public int compareTo(AnimationsStateListener o) {

    	return 0;
    }
}
